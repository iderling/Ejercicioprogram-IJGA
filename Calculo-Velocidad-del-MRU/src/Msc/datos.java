package Msc;

public class datos {
	
    private int d;
	private int v;
	private int t;
	
	public datos(int d, int v, int t) {
		super();
		this.d = d;
		this.v = v;
		this.t = t;
	}

	public int getD() {
		return d;
	}

	public void setD(int d) {
		this.d = d;
	}

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}

	public int getT() {
		return t;
	}

	public void setT(int t) {
		this.t = t;
	}
	

}
